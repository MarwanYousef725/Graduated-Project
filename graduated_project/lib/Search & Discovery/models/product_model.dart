class Autogenerated {
  List<PharmacyProducts>? pharmacyProducts;
  Metadata? metadata;

  Autogenerated({this.pharmacyProducts, this.metadata});

  Autogenerated.fromJson(Map<String, dynamic> json) {
    if (json['pharmacy_products'] != null) {
      pharmacyProducts = <PharmacyProducts>[];
      json['pharmacy_products'].forEach((v) {
        pharmacyProducts!.add(PharmacyProducts.fromJson(v));
      });
    }
    metadata = json['metadata'] != null
        ? Metadata.fromJson(json['metadata'])
        : null;
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = {};
    if (pharmacyProducts != null) {
      data['pharmacy_products'] = pharmacyProducts!
          .map((v) => v.toJson())
          .toList();
    }
    if (metadata != null) {
      data['metadata'] = metadata!.toJson();
    }
    return data;
  }
}

class PharmacyProducts {
  int? id;
  String? name;
  String? category;
  String? description;
  String? activeIngredient;
  String? dosageForm;
  String? strength;
  String? packSize;
  String? manufacturer;
  List<String>? indications;
  String? dosageInstructions;
  SideEffects? sideEffects;
  List<String>? contraindications;
  String? storage;
  bool? prescriptionRequired;
  num? priceUsd;
  num? priceEgp;
  double? rating;
  int? reviews;
  int? stockQuantity;
  String? availability;
  List<String>? images;
  bool? isFavorite;

  PharmacyProducts({
    this.id,
    this.name,
    this.category,
    this.description,
    this.activeIngredient,
    this.dosageForm,
    this.strength,
    this.packSize,
    this.manufacturer,
    this.indications,
    this.dosageInstructions,
    this.sideEffects,
    this.contraindications,
    this.storage,
    this.prescriptionRequired,
    this.priceUsd,
    this.priceEgp,
    this.rating,
    this.reviews,
    this.stockQuantity,
    this.availability,
    this.images,
    this.isFavorite,
  });

  PharmacyProducts.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    name = json['name'];
    category = json['category'];
    description = json['description'];
    activeIngredient = json['active_ingredient'];
    dosageForm = json['dosage_form'];
    strength = json['strength'];
    packSize = json['pack_size'];
    manufacturer = json['manufacturer'];
    indications = json['indications'].cast<String>();
    dosageInstructions = json['dosage_instructions'];
    sideEffects = json['side_effects'] != null
        ? SideEffects.fromJson(json['side_effects'])
        : null;
    contraindications = json['contraindications'].cast<String>();
    storage = json['storage'];
    prescriptionRequired = json['prescription_required'];
    priceUsd = json['price_usd'] ?? 0.0;
    priceEgp = json['price_egp'] ?? 0;
    rating = (json['rating'] as num?)?.toDouble();
    reviews = json['reviews'];
    stockQuantity = json['stock_quantity'];
    availability = json['availability'];
    images = json['images'].cast<String>();
    isFavorite = json['isFavorite'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = {};
    data['id'] = id;
    data['name'] = name;
    data['category'] = category;
    data['description'] = description;
    data['active_ingredient'] = activeIngredient;
    data['dosage_form'] = dosageForm;
    data['strength'] = strength;
    data['pack_size'] = packSize;
    data['manufacturer'] = manufacturer;
    data['indications'] = indications;
    data['dosage_instructions'] = dosageInstructions;
    if (sideEffects != null) {
      data['side_effects'] = sideEffects!.toJson();
    }
    data['contraindications'] = contraindications;
    data['storage'] = storage;
    data['prescription_required'] = prescriptionRequired;
    data['price_usd'] = priceUsd;
    data['price_egp'] = priceEgp;
    data['rating'] = rating;
    data['reviews'] = reviews;
    data['stock_quantity'] = stockQuantity;
    data['availability'] = availability;
    data['images'] = images;
    data['isFavorite'] = isFavorite;
    return data;
  }

  /// Convenience getter: true only when availability is "in_stock"
  bool get isInStock => availability == 'in_stock';

  /// Convenience getter: true only when availability is "low_stock"
  bool get isLowStock => availability == 'low_stock';

  /// Convenience getter: true only when availability is "out_of_stock"
  bool get isOutOfStock => availability == 'out_of_stock';
}

