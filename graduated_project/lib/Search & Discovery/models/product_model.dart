class Metadata {
  int? totalProducts;
  List<String>? currencyOptions;
  String? dataCreated;
  String? dataUpdated;
  NewFields? newFields;
  String? disclaimer;
  List<String>? categories;

  Metadata({
    this.totalProducts,
    this.currencyOptions,
    this.dataCreated,
    this.dataUpdated,
    this.newFields,
    this.disclaimer,
    this.categories,
  });

  Metadata.fromJson(Map<String, dynamic> json) {
    totalProducts = json['total_products'];
    currencyOptions = json['currency_options'].cast<String>();
    dataCreated = json['data_created'];
    dataUpdated = json['data_updated'];
    newFields = json['new_fields'] != null
        ? NewFields.fromJson(json['new_fields'])
        : null;
    disclaimer = json['disclaimer'];
    categories = json['categories'].cast<String>();
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = {};
    data['total_products'] = totalProducts;
    data['currency_options'] = currencyOptions;
    data['data_created'] = dataCreated;
    data['data_updated'] = dataUpdated;
    if (newFields != null) {
      data['new_fields'] = newFields!.toJson();
    }
    data['disclaimer'] = disclaimer;
    data['categories'] = categories;
    return data;
  }
}

